/**
 * Шаблон промпта для LLM (критерий «Prompt Engineering»).
 * В проде подставлять mood и вызывать API; здесь — единый источник формулировки задачи для ИИ.
 */
export function buildAiMeditationPrompt(mood: 'calm' | 'neutral' | 'stressed'): string {
  const moodRu =
    mood === 'calm' ? 'спокойное' : mood === 'stressed' ? 'напряжённое' : 'нейтральное';

  return [
    'Ты — мягкий гид по медитации для мобильного приложения ZenPulse.',
    `Настроение пользователя сейчас: ${moodRu}.`,
    'Сгенерируй ОДНУ короткую аффирмацию или мини-инструкцию дыхания (2–4 предложения), без медицинских обещаний, без токсичного позитива.',
    'Тон: спокойный, поддерживающий. Язык: русский.',
  ].join(' ');
}
